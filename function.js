// Non-Return Type Function
function addTwoNumbers(num1, num2) {
  let sum = num1 + num2;

  console.log(sum);
}

addTwoNumbers(5, 10);

//Return Type Function
function sumTwoNumbers(num1, num2) {
  let sum = num1 + num2;

  return sum;
}

var addNum = sumTwoNumbers(50, 10);

console.log(addNum);

